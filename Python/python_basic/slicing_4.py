jumin = "999021-1234567"

print("성별 : " + jumin[7]) # 인덱스 위치

print("연 : " + jumin[0:2]) # 0에서 2개의 인덱스 가져오기
print("월 : " + jumin[2:4])
print("월 : " + jumin[4:6])

print("생년월일 : " + jumin[:6]) # 인덱스 시작이 0인경우 생략가능
print("생년월일 : " + jumin[7:]) # 인덱스 시작위치부터 끝 까지인경우 콜론 뒷부분 생략가능
print("생년월일 : " + jumin[-7:]) # 혹은 -인덱스로 시작해서 가져오기





